#include <iostream>
#include <vector>
#include <queue>

using namespace std;

#define fio() ios_base::sync_with_stdio(0), cin.tie(0)

int dx[4] = { 1, -1, 0, 0 };
int dy[4] = { 0, 0, 1, -1 };

int main()
{
    fio();

    int n, m; cin >> n >> m;
    int x, y;
    vector<vector<int>> map(n+2, vector<int> (m+2));
    vector<vector<int>> score(n+2, vector<int> (m+2));

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) { 
            cin >> map[i][j];
            if (map[i][j] == 2) { y = i; x = j; }
        }
    }

    queue<pair<int,int>> q;
    q.push({x, y});
    
    while(!q.empty()) {
        int cx = q.front().first, cy = q.front().second; q.pop();
        for (int i = 0; i < 4; i++) {
            int nx = cx + dx[i];
            int ny = cy + dy[i];

            if (map[ny][nx] == 1) {
                map[ny][nx] = 0;
                score[ny][nx] = score[cy][cx]+1;
                q.push({nx,ny});
            }
        }
    }

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (map[i][j] == 1) score[i][j] = -1;
        }
    }

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) cout << score[i][j] << ' ';
        cout << '\n';
    }

    return 0;
}