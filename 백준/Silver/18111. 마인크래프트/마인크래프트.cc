#include <iostream>
#include <vector>

using namespace std;

#define fio() do { ios_base::sync_with_stdio(false); cin.tie(nullptr); cout.tie(nullptr); } while(0)

int earth[500][500];
int height[264];

int main()
{
    fio();
    int N, M, B;
    cin >> N >> M >> B;
    for (int i = 0; i < N; i++)
        for (int j = 0; j < M; j++)
            cin >> earth[i][j];

    for (int i = 0; i < 257; i++) {
        int time = 0;
        int used_block = 0;
        for (int j = 0; j < N && time != -1; j++)
            for (int k = 0; k < M && time != -1; k++) {
                if (earth[j][k] > i) { time += 2 * (earth[j][k] - i); used_block -= earth[j][k] - i; }
                else if (earth[j][k] < i) {
                    used_block += i - earth[j][k];
                    time += i - earth[j][k];
                }
            }
        if (used_block > B) time = -1;
        height[i] = time;
    }

    int h_max, time_min = 64*1000000;
    for (int i = 0; i < 257; i++)
        if (height[i] != -1 && height[i] <= time_min) { h_max = i; time_min = height[i]; }
    cout << time_min << ' ' << h_max;
    return 0;
}